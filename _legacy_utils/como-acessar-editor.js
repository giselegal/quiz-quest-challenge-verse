/**
 * INSTRUÇÕES PARA ACESSAR O EDITOR DE PÁGINA DE RESULTADO
 * =====================================================
 * 
 * 1. CONFIGURE UM USUÁRIO ADMINISTRADOR:
 *    - Execute o script set-admin-user.js no console do navegador
 *    - Para fazer isso, abra o console (F12) em qualquer página do site
 *    - Cole o conteúdo do arquivo set-admin-user.js no console e pressione Enter
 *    - Ou acesse diretamente a URL /resultado/editor
 * 
 * 2. ACESSE O EDITOR:
 *    - Vá para a página de resultados (/resultado)
 *    - Você verá um botão "Editar Página" no topo
 *    - Clique neste botão para abrir o editor
 *    - Ou acesse diretamente a URL /resultado/editor
 * 
 * 3. USE O EDITOR:
 *    - Adicione componentes do painel esquerdo
 *    - Configure as propriedades no painel direito
 *    - Reorganize os blocos arrastando-os
 *    - Alterne entre visualizações desktop e mobile
 *    - Salve suas alterações com o botão Salvar
 * 
 * 4. SOLUÇÃO DE PROBLEMAS:
 *    - Se o editor não abrir, certifique-se de ter executado o script set-admin-user.js
 *    - Verifique o console (F12) para mensagens de erro
 *    - Tente limpar o cache do navegador e tentar novamente
 *    - As alterações são salvas no localStorage, certifique-se de não estar em modo anônimo
 */

